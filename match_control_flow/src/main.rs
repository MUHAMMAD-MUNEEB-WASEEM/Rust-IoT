fn main() {

 let x = 2;                     //assigned 2 to x

 match x {
     1 => println!("one!"),     // if 1, then prints one
     2 => println!("two!"),     // if 2, then prints two
     3 => println!("three!"),   // if 3, then prints three
     _ => println!("None"),     // else, then prints none if no value matches
    

 }



}
